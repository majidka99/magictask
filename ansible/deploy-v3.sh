#!/bin/bash

# MajiTask v3.0 Deployment Script
# This script deploys the upgraded MajiTask application with PostgreSQL and enhanced security

set -e

# Configuration
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
